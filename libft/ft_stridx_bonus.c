int				ft_stridx(const char *src, int c)
{
	int			idx;

	idx = 0;
	if (!src)
		return (-1);
	while (1)
	{
		if (src[idx] == c)
			return (idx);
		if (src[idx] == 0)
			break ;
		idx++;
	}
	return (-1);
}
